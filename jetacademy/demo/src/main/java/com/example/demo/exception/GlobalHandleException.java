package com.example.demo.exception;

import com.example.demo.dto.CapacityDto;
import com.example.demo.dto.ExceptionResponseDto;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.MethodArgumentNotValidException;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.bind.annotation.ResponseStatus;
import org.springframework.web.bind.annotation.RestControllerAdvice;

import java.util.HashMap;
import java.util.Map;

@RestControllerAdvice
public class GlobalHandleException {
    @ExceptionHandler(MethodArgumentNotValidException.class)
    public ResponseEntity<?> notValid(MethodArgumentNotValidException ex) {
        Map<String, String> map = new HashMap<>();
        ex.getBindingResult().getFieldErrors().forEach(e -> map.put(e.getField(), e.getDefaultMessage()));
        return ResponseEntity.badRequest().body(map);
    }

    @ExceptionHandler(NotFountNotebook.class)
    @ResponseStatus(HttpStatus.NOT_FOUND)
    public  ExceptionResponseDto notFound(NotFountNotebook ex){
         return new ExceptionResponseDto(HttpStatus.NOT_FOUND,ex.getMessage());
    }


    @ExceptionHandler(CapasityNotEnough.class)


    public CapacityDto notEnough(CapasityNotEnough ex){
        return new CapacityDto(ex.getMessage(),HttpStatus.NOT_FOUND);
    }
}





















































































































































































































































































































































































































